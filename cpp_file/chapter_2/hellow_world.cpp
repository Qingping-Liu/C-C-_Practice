
#include <iostream>
// 将iostream标准库添加到程序中
// iostream包含输入和输出

using namespace std;
// 命名空间

int main()
{
    cout << "Hello" << "World" << endl;
    // cout是在屏幕输出
    // endl是控制符，表示换行（\n）
    // endl是1：换行\n，2：fflush(stdin)
    // \n 是 换行
    // 把字符串插入到输出流里面, 输出是一种流
    
    cout << "" << endl;

    return 0;
    // return 0 表示正常结束
}


